	// Persistant
	i3Animation *				m_pAnim = nullptr;
	INT32						m_idxBone = -1;

	I3_ANIM_PLAY_MODE			m_PlayMode = I3_ANIM_PLAY_MODE_REPEAT;
	I3_ANIM_BLEND_MODE			m_BlendMode = I3_ANIM_BLEND_MODE_REPLACE;

	// Volatile
	MATRIX					m_CacheMatrix;
	REAL32						m_timeLocal = 0.0f;
	UINT32						m_PlayCount = 0;
	bool						m_bFirst = true;

