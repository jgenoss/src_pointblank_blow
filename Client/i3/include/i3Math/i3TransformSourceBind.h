public:
	virtual ~i3TransformSourceBind(void);

	////////////////////////////////////////////////////////////////////////////////////////////////////
	/// \fn	void i3TransformSourceBind::Init( REAL32 tm = 0.0f);
	///
	/// \brief	Initialises this object.
	///
	/// \author	
	/// \date	2011-12-08
	///
	/// \param	tm	(optional) the time.
