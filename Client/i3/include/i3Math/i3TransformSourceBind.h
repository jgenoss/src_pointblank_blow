	////////////////////////////////////////////////////////////////////////////////////////////////////
	/// \fn	REAL32 i3TransformSourceBind::getLocalTime(void)
	///
	/// \brief	Gets the local time.
	///
	/// \author	
	/// \date	2011-12-08
	///
	/// \return	The local time.
	////////////////////////////////////////////////////////////////////////////////////////////////////

