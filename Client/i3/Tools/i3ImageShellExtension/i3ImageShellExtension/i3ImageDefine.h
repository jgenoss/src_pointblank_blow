#include <d3d9.h>
#include <d3dx9.h>

#pragma once

#define I3I_SIGNATURE_SIZE			4
#define I3I_SIGNATURE_BINARY		"I3IB"
#define I3I_SIGNATURE_EXTERNAL		"I3IE"

#define		I3I_TEXFLAG_CUBE			0x00000001
#define		I3I_TEXFLAG_VOLUME			0x00000002

#define		I3I_TEXFLAG_NORMAL			0x10000000
#define		I3I_TEXFLAG_SPECULAR		0x20000000
#define		I3I_TEXFLAG_ENVIRONMENT		0x40000000
#define		I3I_TEXFLAG_REFLECT			0x80000000
#define		I3I_TEXFLAG_REFLECT_MASK	0x01000000
#define		I3I_TEXFLAG_LUX				0x02000000
#define		I3I_TEXFLAG_EMISSIVE		0x04000000
#define		I3I_TEXFLAG_MASK_USAGE		0xFF000000

#define I3G_IMAGE_FORMAT_MASK_INDEXED				0x10000000
#define I3G_IMAGE_FORMAT_MASK_ALPHA					0x20000000
#define I3G_IMAGE_FORMAT_MASK_DEPTH					0x40000000
#define I3G_IMAGE_FORMAT_MASK_COMPRESSED			0x80000000
#define I3G_IMAGE_FORMAT_MASK_ZLIB					0x01000000
#define I3G_IMAGE_FORMAT_MASK_HDRI					0x02000000

#define I3G_IMAGE_FORMAT_MASK_PC					0x00010000
#define I3G_IMAGE_FORMAT_MASK_PSP					0x00020000
#define I3G_IMAGE_FORMAT_MASK_PS2					0x00040000
#define I3G_IMAGE_FORMAT_MASK_XBOX					0x00080000
#define I3G_IMAGE_FORMAT_MASK_XBOX2					0x00100000

#define	I3G_IMAGE_FORMAT_MASK_PLATFORM				0x00FF0000

enum I3G_IMAGE_FORMAT
{
	I3G_IMAGE_FORMAT_NONE = 0,
	I3G_IMAGE_FORMAT_DONT_CARE = 0,

	I3G_IMAGE_FORMAT_DEFAULT_COLOR = (0x0001 | I3G_IMAGE_FORMAT_MASK_ALPHA),

	// -------------------------
	// General(Linear) Formats
	// -------------------------

	// 4~8 bit format
	I3G_IMAGE_FORMAT_I4 = (0x0101 | I3G_IMAGE_FORMAT_MASK_INDEXED),	// Indexed
	I3G_IMAGE_FORMAT_I8 = (0x0102 | I3G_IMAGE_FORMAT_MASK_INDEXED), // Indexed
	i3G_IMAGE_FORMAT_L8 = (0x0103), // Luminance
	i3G_IMAGE_FORMAT_A8 = (0x0104 | I3G_IMAGE_FORMAT_MASK_ALPHA), // Alpha

	// 16 bit formats
	I3G_IMAGE_FORMAT_RGB_565 = (0x0201),
	I3G_IMAGE_FORMAT_BGR_565 = (0x0202),
	I3G_IMAGE_FORMAT_XRGB_1555 = (0x0203),
	I3G_IMAGE_FORMAT_ARGB_1555 = (0x0204 | I3G_IMAGE_FORMAT_MASK_ALPHA),
	I3G_IMAGE_FORMAT_XBGR_1555 = (0x0205),
	I3G_IMAGE_FORMAT_ABGR_1555 = (0x0206 | I3G_IMAGE_FORMAT_MASK_ALPHA),
	I3G_IMAGE_FORMAT_XRGB_4444 = (0x0207),
	I3G_IMAGE_FORMAT_ARGB_4444 = (0x0208 | I3G_IMAGE_FORMAT_MASK_ALPHA),
	I3G_IMAGE_FORMAT_XBGR_4444 = (0x0209),
	I3G_IMAGE_FORMAT_ABGR_4444 = (0x020A | I3G_IMAGE_FORMAT_MASK_ALPHA),

	// 24 bit formats
	I3G_IMAGE_FORMAT_RGB_888 = (0x0301),
	I3G_IMAGE_FORMAT_BGR_888 = (0x0302),

	// 32 bit formats
	I3G_IMAGE_FORMAT_RGBX_8888 = (0x0401),
	I3G_IMAGE_FORMAT_BGRX_8888 = (0x0402),
	I3G_IMAGE_FORMAT_RGBA_8888 = (0x0405 | I3G_IMAGE_FORMAT_MASK_ALPHA),
	I3G_IMAGE_FORMAT_BGRA_8888 = (0x0406 | I3G_IMAGE_FORMAT_MASK_ALPHA),

	// -------------------------
	// Bump-map
	// -------------------------
	I3G_IMAGE_FORMAT_VU_88 = (0x0501),
	I3G_IMAGE_FORMAT_QWVU_8888 = (0x0502),

	// -------------------------
	// Compressed formats
	// -------------------------
	I3G_IMAGE_FORMAT_DXT1_X = (0x0680 | I3G_IMAGE_FORMAT_MASK_COMPRESSED),	// RGB Only
	I3G_IMAGE_FORMAT_DXT1 = (0x0681 | I3G_IMAGE_FORMAT_MASK_COMPRESSED | I3G_IMAGE_FORMAT_MASK_ALPHA),	// 1 bit alpha
	I3G_IMAGE_FORMAT_DXT2 = (0x0601 | I3G_IMAGE_FORMAT_MASK_COMPRESSED | I3G_IMAGE_FORMAT_MASK_ALPHA),
	I3G_IMAGE_FORMAT_DXT3 = (0x0602 | I3G_IMAGE_FORMAT_MASK_COMPRESSED | I3G_IMAGE_FORMAT_MASK_ALPHA),
	I3G_IMAGE_FORMAT_DXT4 = (0x0603 | I3G_IMAGE_FORMAT_MASK_COMPRESSED | I3G_IMAGE_FORMAT_MASK_ALPHA),
	I3G_IMAGE_FORMAT_DXT5 = (0x0604 | I3G_IMAGE_FORMAT_MASK_COMPRESSED | I3G_IMAGE_FORMAT_MASK_ALPHA),

	// -------------------------
	// HDR
	// -------------------------
	I3G_IMAGE_FORMAT_ABGR_16F = (0x0801 | I3G_IMAGE_FORMAT_MASK_ALPHA),
	I3G_IMAGE_FORMAT_ABGR_32F = (0x0802 | I3G_IMAGE_FORMAT_MASK_ALPHA),
	I3G_IMAGE_FORMAT_ABGR_16 = (0x0803 | I3G_IMAGE_FORMAT_MASK_ALPHA),
	I3G_IMAGE_FORMAT_GR_16F = (0x0804),
	I3G_IMAGE_FORMAT_R_32F = (0x0805),
	I3G_IMAGE_FORMAT_GR_32F = (0x0806),

	// -------------------------
	// depth, stencil, vertex buffers
	// -------------------------
	I3G_IMAGE_FORMAT_D16 = (0x0700 | I3G_IMAGE_FORMAT_MASK_DEPTH),
	I3G_IMAGE_FORMAT_D24S8 = (0x0701 | I3G_IMAGE_FORMAT_MASK_DEPTH),
	I3G_IMAGE_FORMAT_D24X8 = (0x0702 | I3G_IMAGE_FORMAT_MASK_DEPTH),
	I3G_IMAGE_FORMAT_D32 = (0x0703 | I3G_IMAGE_FORMAT_MASK_DEPTH),
	I3G_IMAGE_FORMAT_D32F = (0x0704 | I3G_IMAGE_FORMAT_MASK_DEPTH),
	I3G_IMAGE_FORMAT_D24FS8 = (0x0705 | I3G_IMAGE_FORMAT_MASK_DEPTH),

	// -------------------------
	// YUV... for movie
	// -------------------------

	// -------------------------
	// Swizzled (Xbox)
	// -------------------------

	I3G_IMAGE_FORMAT_FORCE_DWORD = 0xFFFFFFFF
};

#pragma pack (push, 1)

typedef struct _tagI3I_HEADER
{
	INT8				m_Signature[I3I_SIGNATURE_SIZE];
	UINT8				m_Version[2];
	UINT16				m_Width;
	UINT16				m_Height;
	I3G_IMAGE_FORMAT	m_Format;

	UINT16				m_ClutCount;
} I3I_FILE_HEADER;

