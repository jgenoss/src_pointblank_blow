{
	I3I_FILE_HEADER		m_OldVersionHeader;

	UINT32				m_Flags;
	UINT32				m_FileSize;
	UINT16				m_MipMapCount;
	UINT16				m_NameLength;
} I3I_FILE_HEADER2;

